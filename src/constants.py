def get_hour_branch(hour):
    """根据小时(0-23)返回时辰地支"""
    if hour >= 23 or hour < 1: return "子"
    elif hour < 3: return "丑"
    elif hour < 5: return "寅"
    elif hour < 7: return "卯"
    elif hour < 9: return "辰"
    elif hour < 11: return "巳"
    elif hour < 13: return "午"
    elif hour < 15: return "未"
    elif hour < 17: return "申"
    elif hour < 19: return "酉"
    elif hour < 21: return "戌"
    else: return "亥"

