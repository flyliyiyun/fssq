import csv as _csv

_STOCK_DATA_PATH = os.path.join(DATA_DIR, "A股所有股票板块分类_含五行属性.csv")
_STOCK_CACHE = None

def load_stock_pool():
    """加载A股股票池数据（懒加载，自动过滤老三板和五行未知股票）"""
    global _STOCK_CACHE
    if _STOCK_CACHE is not None:
        return _STOCK_CACHE

    _STOCK_CACHE = []
    _EXCLUDE_SUFFIXES = (".NQ", ".BJ")  # 老三板/退市股不纳入选股池
    try:
        with open(_STOCK_DATA_PATH, "r", encoding="utf-8-sig") as f:
            reader = _csv.DictReader(f)
            for row in reader:
                element = row.get("五行属性", "").strip()
                # 跳过五行未知或为空的股票
                if not element or element in ("未知", "五行属性"):
                    continue

                secucode = row.get("SECUCODE", "")
                # 跳过老三板（.NQ/.BJ）
                if any(secucode.endswith(suffix) for suffix in _EXCLUDE_SUFFIXES):
                    continue

                if row.get("SECURITY_CODE"):
                    # 使用SECUCODE（含后缀）用于行情API查询
                    code = secucode if secucode else row["SECURITY_CODE"]
                    # 如果SECUCODE为空，尝试根据交易所推断后缀
                    if not secucode:
                        if row["SECURITY_CODE"].startswith("6"):
                            code = f"{row['SECURITY_CODE']}.SH"
                        else:
                            code = f"{row['SECURITY_CODE']}.SZ"
                    _STOCK_CACHE.append({
                        "code": code,
                        "name": row["SECURITY_NAME_ABBR"],
                        "board": row["BOARD_NAME"],
                        "board_code": row["BOARD_CODE"],
                        "market": row.get("TRADE_MARKET", ""),
                        "element": element,
                        "secucode": row.get("SECUCODE", "")
                    })
    except FileNotFoundError:
        _STOCK_CACHE = []

    return _STOCK_CACHE

def get_stocks_by_element(element, limit=50):
    """获取指定五行属性的股票"""
    pool = load_stock_pool()
    return [s for s in pool if s["element"] == element][:limit]

def get_stock_by_code(code):
    """根据代码查询股票"""
    pool = load_stock_pool()
    for s in pool:
        if s["code"] == code or s["secucode"] == code:
            return s
    return None
